/**
 * Generates src/i18n/enToPlTileNames.ts from scripts/tile-names-unique.json + scripts/pl-lines.txt
 * Run: node scripts/build-en-pl-tile-names.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const names = JSON.parse(fs.readFileSync(path.join(root, 'scripts/tile-names-unique.json'), 'utf8'));
const plLines = fs
  .readFileSync(path.join(root, 'scripts/pl-lines.txt'), 'utf8')
  .trim()
  .split(/\n/)
  .map((l) => l.trim());

if (names.length !== plLines.length) {
  console.error(`Mismatch: ${names.length} English names vs ${plLines.length} Polish lines`);
  process.exit(1);
}

const entries = names.map((en, i) => [en, plLines[i]]);
const obj = Object.fromEntries(entries);

const out = `/** Auto-generated by scripts/build-en-pl-tile-names.mjs — do not edit by hand. */
export const EN_TO_PL_TILE_NAME: Record<string, string> = ${JSON.stringify(obj, null, 2)};

export function polishTileNameFromEnglish(english: string): string {
  return EN_TO_PL_TILE_NAME[english] ?? english;
}
`;

fs.writeFileSync(path.join(root, 'src/i18n/enToPlTileNames.ts'), out);
console.log('Wrote src/i18n/enToPlTileNames.ts', Object.keys(obj).length, 'entries');
